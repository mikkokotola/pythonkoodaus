# Laskeminen luvuilla, kerta 3

# Minkälaisia laskuja voi laskea
print(5+2) # Summa
print(5-2) # Erotus
print(5*2) # Tulo
print(5/2) # Osamäärä / jakolasku
print(5//2) # Jakolaskun kokonaislukuosa
print(5%2) # Jakojäännös

# Laskujärjestys
print(2 + 2 * 3)
print((2 + 2) * 3)

# Liukuluvut = desimaaliluvut
luku1 = 4.0
luku2 = 1.5

tulos = luku1 - luku2
print(f"Tulos on {tulos}")
print(f"{luku1} - {luku2} = {tulos}")
